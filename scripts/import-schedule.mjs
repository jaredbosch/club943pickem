// ET → UTC. EDT (UTC-4) for Sep-Oct; EST (UTC-5) for Nov-Mar.
function toUtcIso(date, h, min) {
  const [y, mo, d] = date.split('-').map(Number);
  const offsetH = (mo >= 3 && mo <= 10) ? 4 : 5;
  const utcH = h + offsetH;
  const dt = new Date(Date.UTC(y, mo - 1, d, utcH, min));
  return dt.toISOString();
}

// Determine time_slot from ET weekday + hour.
function weekdayOf(date) {
  const [y, mo, d] = date.split('-').map(Number);
  return new Date(Date.UTC(y, mo - 1, d, 12))
    .toLocaleDateString('en-US', { timeZone: 'America/New_York', weekday: 'short' });
}

function timeSlot(date, h) {
  const wd = weekdayOf(date);
  switch (wd) {
    case 'Wed': case 'Thu': case 'Fri': case 'Sat': return 'thursday';
    case 'Sun':
      if (h < 12) return 'intl';
      if (h < 16) return 'sunday_early';
      if (h < 20) return 'sunday_late';
      return 'sunday_night';
    case 'Mon': return 'monday';
    default: throw new Error(`Unexpected weekday ${wd} for ${date}`);
  }
}


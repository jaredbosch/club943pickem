async function main() {
  // Fetch all existing 2026 games
  const { data: existing, error: fetchErr } = await supabase
    .from('games')
    .select('away_team, home_team, week, season_year')
    .eq('season_year', 2026);

  if (fetchErr) throw new Error(`Failed to fetch existing games: ${fetchErr.message}`);

  const existingKeys = new Set(
    existing.map(g => `${g.away_team}-${g.home_team}-${g.week}`)
  );

  console.log(`Existing 2026 games in DB: ${existing.length}`);

  let inserted = 0;
  let skipped = 0;
  const errors = [];

  for (const [week, away, home, date, h, min] of SCHEDULE) {
    const key = `${away}-${home}-${week}`;
    if (existingKeys.has(key)) {
      skipped++;
      continue;
    }

    const kickoff_time = toUtcIso(date, h, min);
    const time_slot = timeSlot(date, h);
    const row = {
      week,
      season_year: 2026,
      away_team: away,
      home_team: home,
      kickoff_time,
      time_slot,
      spread_home: null,
      status: 'scheduled',
    };

    const { error } = await supabase.from('games').insert(row);
    if (error) {
      errors.push({ key, error: error.message });
    } else {
      inserted++;
    }
  }

  console.log(`\nInserted: ${inserted}`);
  console.log(`Skipped (already existed): ${skipped}`);
  if (errors.length) {
    console.log(`Errors: ${errors.length}`);
    errors.forEach(e => console.log(`  ${e.key}: ${e.error}`));
  } else {
    console.log('No errors.');
  }
}

main().catch(console.error);
